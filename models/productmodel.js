// models/Policy.js

const mongoose = require("mongoose");

const UserSchema = new mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
      unique: true,
    },
    password: {
      type: String,
      required: true,
    },
  },
  { timestamps: true }
);

const PolicySchema = new mongoose.Schema(
  {
    id: {
      type: String,
      required: true,
    },
    policy: {
      type: String,
      required: true,
    },
    amount: {
      type: Number,
      required: true,
    },
    status: {
      type: String,
      enum: ["active", "inactive"],
      default: "active",
    },
  },
  { timestamps: true }
);

const Policy = mongoose.model("Policy", PolicySchema);
const User = mongoose.model("User", UserSchema);
module.exports = { Policy, User };
